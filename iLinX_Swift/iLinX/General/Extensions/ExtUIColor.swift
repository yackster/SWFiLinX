import UIKit

extension UIColor {
    
    public class var iLinXBGColor: UIColor{
        return UIColor(red: 238/255, green: 242/255, blue: 253/255, alpha: 1.0)
    }
    
    public class var iLinXBlueColor: UIColor{
        return UIColor(red: 0, green: 120/255, blue: 255/255, alpha: 1.0)
    }
    
    public class var iLinXGrayColor: UIColor{
        return UIColor(red: 84/255, green: 110/255, blue: 122/255, alpha: 1.0)
    }
    
    public class var iLinXRedColor: UIColor{
        return UIColor(red: 1, green: 0, blue: 0, alpha: 1.0)
    }
    
}
